// ฟังก์ชันที่ใช้ในการแสดงรายละเอียดการจองใน popup
function displayBookingDetails() {

  // ดึงค่าจาก form
  const origin = document.getElementById("origin").value;
  const destination = document.getElementById("destination").value;
  const departureDate = document.getElementById("departure-date").value;
  const departureTime = document.getElementById("departure-time").value;
  const selectClass = document.getElementById("selectclass").value;
  const numPassengers = document.getElementById("num-passengers").value;
  const price = document.getElementById("price").value;

  // ตรวจสอบว่ากรอกครบไหม
  if (!origin || !destination || !departureDate || !departureTime || !selectClass || !numPassengers || !price) {
    document.getElementById("error-popup").style.display = "block";
    document.getElementById("popup-overlay").style.display = "block";
    return;
  }

  // สร้างข้อมูลการจอง
  const bookingData = {
    code: generateRandomCode(8),
    origin,
    destination,
    date: departureDate,
    time: departureTime,
    class: selectClass,
    passengers: numPassengers,
    price
  };

  // บันทึกลง localStorage
  const history = JSON.parse(localStorage.getItem("bookingHistory")) || [];
  history.push(bookingData);
  localStorage.setItem("bookingHistory", JSON.stringify(history));

  // แสดงข้อมูลใน popup
  document.getElementById("popup-content").innerHTML = `
    <strong>รหัสจอง:</strong> ${bookingData.code}<br>
    <strong>ต้นทาง:</strong> ${origin}<br>
    <strong>ปลายทาง:</strong> ${destination}<br>
    <strong>วันเดินทาง:</strong> ${departureDate}<br>
    <strong>เวลา:</strong> ${departureTime}<br>
    <strong>คลาส:</strong> ${selectClass}<br>
    <strong>จำนวนผู้โดยสาร:</strong> ${numPassengers}<br>
    <strong>ราคา:</strong> ${price}
  `;

  document.getElementById("popup").style.display = "block";
  document.getElementById("popup-overlay").style.display = "block";
}



