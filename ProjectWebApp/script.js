function showQRPopup() {
  const price = document.getElementById("price").value;
  const origin = document.getElementById("origin").value;
  const destination = document.getElementById("destination").value;
  const date = document.getElementById("departure-date").value;
  const time = document.getElementById("departure-time").value;
  const passengers = document.getElementById("num-passengers").value;

  // ข้อความที่จะแปลงเป็น QR
  const qrText = `ชำระเงินตั๋วจาก ${origin} ไป ${destination}\nวันที่: ${date} เวลา: ${time}\nจำนวนผู้โดยสาร: ${passengers}\nราคา: ${price} บาท`;

  // แปลงข้อความเป็น URL สำหรับ QR Code API
  const qrUrl = `https://api.qrserver.com/v1/create-qr-code/?data=${encodeURIComponent(qrText)}&size=250x250`;

  // เปลี่ยน src รูปภาพ
  document.getElementById("qr-image").src = qrUrl;

  // แสดง popup
  document.getElementById("qr-popup").style.display = "block";
  document.getElementById("qr-popup-overlay").style.display = "block";
}

function closeQRPopup() {
  document.getElementById("qr-popup").style.display = "none";
  document.getElementById("qr-popup-overlay").style.display = "none";
}
function payNow() {
  // ซ่อน popup รายละเอียด
  document.getElementById("popup").style.display = "none";
  document.getElementById("popup-overlay").style.display = "none";

  // แสดง QR popup
  showQRPopup();
}


