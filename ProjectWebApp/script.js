// ฟังก์ชันคำนวณราคา
function calculatePrice() {
  try {
      // ดึงค่าที่ผู้ใช้เลือกจากฟอร์ม
      const origin = document.getElementById("origin").value;
      const destination = document.getElementById("destination").value;
      const departureDate = document.getElementById("departure-date").value;
      const departureTime = document.getElementById("departure-time").value;
      const passengers = document.getElementById("num-passengers").value;
      const selectedClass = document.getElementById("selectclass").value;

      // เช็คว่าผู้ใช้กรอกข้อมูลครบหรือยัง
      if (!origin || !destination || !departureDate || !departureTime || !passengers) {
          document.getElementById("price");
          return; 
      }

      // คำนวณราคาจากต้นทางและปลายทาง
      let price = 5; // ราคาเริ่มต้น

      // เพิ่มราคาตามคลาสที่เลือก
      if (selectedClass === "First Class") {
          price += 50;  // เพิ่มราคา 50 บาทสำหรับ First Class
      }

      // คำนวณราคาเพิ่มจากระยะทาง
      if (routes[origin] && routes[origin][destination]) {
          price += routes[origin][destination];
      } else {
          price += 8;  // กรณีเส้นทางไม่อยู่ในรายการ ให้เพิ่มค่าบริการ
      }

      // คูณราคากับจำนวนผู้โดยสาร
      price *= passengers;

      // เรียกข้อมูลภาษาที่เลือก
      const selectedLang = document.getElementById('language').value;
      const t = translations[selectedLang]; // ใช้ชุดคำแปลของภาษาที่เลือก

      // แสดงราคาในช่อง price
      document.getElementById("price").value = price + " " + (selectedLang === "th" ? "บาท" : "THB");

  } catch (error) {
      console.error("เกิดข้อผิดพลาดในการคำนวณราคา:", error);
      document.getElementById("price").value = "เกิดข้อผิดพลาดในการคำนวณราคา";
  }
}

